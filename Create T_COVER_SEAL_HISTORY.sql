CREATE OR ALTER TRIGGER BBCT.T_COVER_PAGE_HISTORY 
ON BBCT.T_COVER_PAGE
AFTER INSERT, DELETE,UPDATE
AS
BEGIN
	DECLARE @INSERTCOUNT int, @DELETEDCOUNT int, 
	@INSERTEDCOVERPAGENO varchar(13), @INSERTEDREMARK varchar(5), @INSERTEDREPLICATECOUNT varchar(10), @INSERTEDDATETAGGED varchar(10), @INSERTEDTAGGEDBY varchar(10), 
	@INSERTEDDATECREATED datetime2(6), @INSERTEDCREATEDBY varchar(10), @INSERTEDDATEDELIVERED date,
	@DELETEDCOVERPAGENO varchar(13), @DELETEDREMARK varchar(5), @DELETEDREPLICATECOUNT varchar(10), @DELETEDDATETAGGED varchar(10), @DELETEDTAGGEDBY varchar(10), 
	@DELETEDDATECREATED datetime2(6), @DELETEDCREATEDBY varchar(10), @DELETEDDATEDELIVERED date
	SET @INSERTCOUNT = (SELECT COUNT(*) FROM INSERTED);
	SET @DELETEDCOUNT = (SELECT COUNT(*) FROM DELETED);

	SET @INSERTEDCOVERPAGENO = (SELECT COVER_PAGE_NO FROM INSERTED);
	SET @INSERTEDREMARK = (SELECT ISNULL(REMARK, 'NULL') FROM INSERTED);
	SET @INSERTEDREPLICATECOUNT = (SELECT CONVERT(varchar(10),REPLICATE_COUNT) FROM INSERTED);
	SET @INSERTEDDATETAGGED = (SELECT CONVERT(varchar(10), DATE_TAGGED, 6) FROM INSERTED);
	SET @INSERTEDTAGGEDBY = (SELECT TAGGED_BY FROM INSERTED);
	SET @INSERTEDDATECREATED = (SELECT DATE_CREATED FROM INSERTED);
	SET @INSERTEDCREATEDBY = (SELECT CREATED_BY FROM INSERTED);
	SET @INSERTEDDATEDELIVERED = (SELECT DATE_DELIVERED FROM INSERTED);

	SET @DELETEDCOVERPAGENO = (SELECT COVER_PAGE_NO FROM DELETED);
	SET @DELETEDREMARK = (SELECT ISNULL(REMARK, 'NULL') FROM DELETED);
	SET @DELETEDREPLICATECOUNT = (SELECT CONVERT(varchar(10),REPLICATE_COUNT) FROM DELETED);
	SET @DELETEDDATETAGGED = (SELECT CONVERT(varchar(10), DATE_TAGGED, 6) FROM DELETED);
	SET @DELETEDTAGGEDBY = (SELECT TAGGED_BY FROM DELETED);
	SET @DELETEDDATECREATED = (SELECT DATE_CREATED FROM DELETED);
	SET @DELETEDCREATEDBY = (SELECT CREATED_BY FROM DELETED);
	SET @DELETEDDATEDELIVERED = (SELECT DATE_DELIVERED FROM DELETED);

	SET NOCOUNT ON;
	
		if (@INSERTCOUNT >0 AND @DELETEDCOUNT > 0)
			BEGIN
				PRINT 'Update';
				if (@INSERTEDCOVERPAGENO  != @DELETEDCOVERPAGENO)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) 
					VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'U', @DELETEDCOVERPAGENO, @INSERTEDCOVERPAGENO , 'Updated ' + ' cover seal from ' + @DELETEDCOVERPAGENO + ' to ' + @INSERTEDCOVERPAGENO, 'CF000000');
				END
				if (@INSERTEDREMARK != @DELETEDREMARK)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'U', @DELETEDREMARK, @INSERTEDREMARK, 'Updated ' +@INSERTEDCOVERPAGENO+ ' remark from ' + @DELETEDREMARK + ' to ' + @INSERTEDREMARK, 'CF000000');
				END
				if (@INSERTEDREPLICATECOUNT  != @DELETEDREPLICATECOUNT)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'U', @DELETEDREPLICATECOUNT, @INSERTEDREPLICATECOUNT, 'Updated ' +@INSERTEDCOVERPAGENO+ ' replicate count from ' + @DELETEDREPLICATECOUNT + ' to ' + @INSERTEDREPLICATECOUNT, 'CF000000');
				END
				if (@INSERTEDDATETAGGED != @DELETEDDATETAGGED)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'U', @DELETEDDATETAGGED, @INSERTEDDATETAGGED, 'Updated ' +@INSERTEDCOVERPAGENO+ ' date tagged from ' + @DELETEDDATETAGGED + ' to ' + @INSERTEDDATETAGGED, 'CF000000');
				END
				if (@INSERTEDTAGGEDBY != @DELETEDTAGGEDBY)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'U', @DELETEDTAGGEDBY, @INSERTEDTAGGEDBY, 'Updated ' +@INSERTEDCOVERPAGENO+ ' tagged by from ' + @DELETEDTAGGEDBY + ' to ' + @INSERTEDTAGGEDBY, 'CF000000');
				END
			END
		else 
			if (@INSERTCOUNT >0 AND @DELETEDCOUNT=0)
				BEGIN
					INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'I', null, @INSERTEDCOVERPAGENO, 'Inserted ' + @INSERTEDCOVERPAGENO, 'CF000000');		
				END
			else 
			if (@INSERTCOUNT=0 AND @DELETEDCOUNT > 0)
			BEGIN
				INSERT INTO BBCT.T_HISTORY (DATE_DONE, TABLE_NAME, ACTIVITY, DATA_FROM, DATA_TO, DESCRIPTION, USERNAME) VALUES (CURRENT_TIMESTAMP, 'T_COVER_PAGE', 'D', @DELETEDCOVERPAGENO, null, 'Deleted ' + @DELETEDCOVERPAGENO, 'CF000000');
			END
END
